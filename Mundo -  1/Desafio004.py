n = input('Digite algo: ')
print('O tipo primitivo desse valor é ', type(n))
print('só tem espaços? {}'.format(n.isspace()))
print('É um número? {}'. format(n.isalnum()))
print('É maiuscula? {}'.format(n.isupper()))
print('É minuscula? {}'.format(n.islower()))
print('É alfanúmerico? {}'.format(n.isalnum()))
print('É alfabético? {}'.format(n.isalpha()))
print('É capitalizada? {}'.format(n.istitle()))
print('É printavél? {}'.format(n.isprintable()))





